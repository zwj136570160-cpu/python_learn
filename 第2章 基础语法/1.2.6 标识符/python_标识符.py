"""
- 标识符是什么
- ①变量的名字
- ②方法的名字
- ③类的名字，等等
- 这些统一称为标识符，用来做内容的标识
- 1.2.6 标识符：是用户在编程的时候所使用的一系列的名字，用于给变量、类、方法等命名
"""

# python中，标识符命名的规则有三类
# ①内容限定
# 标识符命名中，只允许出现：英文、中文、数字、下划线（_)，其余任何内容都不被允许（不推荐中文，数字不可以用在开头）

# ②大小写敏感

# ③不可使用关键字
# python中有一系列单词，称之为关键字，在python中都有特定用途，不可以作为标识符（比如：110、120、119、122，以及相关语法比如print也不可以）

"""
- 变量的命名规范
① 见名知意 （明了：尽量做到，看到名字，就知道什么意思，简洁：尽量在确保明了的前提下，减少名字的长度）
② 下划线命名法（多个单词组合变量名的时候，要使用下划线做分隔）
③ 英文字母全小写
"""
# 不遵守命名规范，会导致报错，不遵守规范，会显得代码不够高级